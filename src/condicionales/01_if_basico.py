"""
Ejercicio 1 – Declaración if básica
Conceptos: if simple, operadores de comparación
"""

# Ejemplo 1: Mayor de edad
edad = 20
if edad >= 18:
    print("Eres mayor de edad.")

# Ejemplo 2: Temperatura
temperatura = 30
if temperatura > 25:
    print("Hace calor hoy.")

# Ejemplo 3: Operadores lógicos básicos con if
hora = 10
if hora >= 6 and hora < 12:
    print("Buenos días.")